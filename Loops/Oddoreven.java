//Odd or Even//
public class Oddoreven
{
public static void main(String[] args)
{
for(int i=0;i<10;i++)
{
if(i%2==0)
{
System.out.println("The Even value:"+i);
}
else
{
System.out.println("The Odd value:"+i);
}
}
}
}
